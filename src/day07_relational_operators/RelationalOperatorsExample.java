package day07_relational_operators;

public class RelationalOperatorsExample {

    public static void main(String[] args) {


        System.out.println(5);
        System.out.println(4);

        System.out.println(5 > 4);   // true
        System.out.println(5 < 4);   // false

        System.out.println( 6 >= 6 );  // true
        System.out.println( 6 <= 6);   // true



        System.out.println( 6 > 6);   // false
        System.out.println( 6 == 6);  //  == vs =

        //System.out.println( 6 = 6);

        int a = 5;  // = ---- > assignment
        System.out.println( 5 == 5); // == ----->  equation for number

        System.out.println( 4 != 4);  // --- >  false -- because 4 == 4
        System.out.println(6 != 7);   // --- > true  -- because 6 < 7






    }

}
